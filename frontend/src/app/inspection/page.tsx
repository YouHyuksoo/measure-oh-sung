// Helper component for status badges
const StatusBadge = ({ status }: { status: string }) => {
  const style = statusStyles[status] || {
    variant: "secondary" as BadgeVariant,
    label: status,
    icon: null,
  };

  return (
    <Badge variant={style.variant} className={style.className}>
      {style.icon}
      {style.label}
    </Badge>
  );
};

