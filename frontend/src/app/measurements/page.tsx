      <div>
        <h1 className="text-3xl font-bold">Ï∏°Ï†ï Îç∞Ïù¥ÌÑ∞</h1>
        <p className="text-muted-foreground">Ï∏°Ï†ï Í≤∞Í≥º Ï°∞Ìöå Î∞è Î∂ÑÏÑù</p>
      </div>

